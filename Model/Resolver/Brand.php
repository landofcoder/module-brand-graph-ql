declare(strict_types=1);

namespace Lof\BrandGraphQl\Model\Resolver;

use Magento\Framework\Exception\NoSuchEntityException;
use Magento\Framework\GraphQl\Config\Element\Field;
use Magento\Framework\GraphQl\Exception\GraphQlInputException;
use Magento\Framework\GraphQl\Exception\GraphQlNoSuchEntityException;
use Magento\Framework\GraphQl\Query\ResolverInterface;
use Magento\Framework\GraphQl\Schema\Type\ResolveInfo;

class Brand implements ResolverInterface
{

    private $brandDataProvider;

    /**
     * @param DataProvider\Brand $brandDataProvider
     */
    public function __construct(DataProvider\Brand $brandDataProvider)
    {
        $this->brandDataProvider = $brandDataProvider;
    }

    /**
     * @inheritdoc
     */
    public function resolve(
        Field $field,
        $context,
        ResolveInfo $info,
        array $value = null,
        array $args = null
    ) {
        return $this->brandDataProvider->getBrand($args['brand_id']);
    }
}
